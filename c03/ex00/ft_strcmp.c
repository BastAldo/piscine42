#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	tots1;
	int	tots2;
	
	while (s1)
	{
		tots1 += *s1;
		s1++;
	}
	while (s1)
	{
		tots2 += *s2;
		s2++;
	}
	return tots1 - tots2;
}
