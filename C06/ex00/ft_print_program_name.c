#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int	argc, char	**argv)
{	
	ft_putstr("argc: ");
	ft_putstr((char)argc + '0');
	ft_putstr("\n argv: ");
	ft_putstr(argv[0]);
	ft_putstr("\n");
	return (0);
}
