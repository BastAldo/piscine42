#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		if (nb == -2147483648)
		{
			ft_putchar(2 + '0');
			nb += 2000000000;
		}
		nb *= -1;
	}
	if (nb >= 10)
	{
		t_putnbr(nb / 10);
	}
	ft_putchar(nb % 10 + '0');
}
